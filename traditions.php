	<h2>Chants</h2>
	<p>Many of our chants during the cadence are shared with the trombones. However, we have a few moves and chants of our own:</p>
	<ul>
	<li>The correct chants for the first verse are, in order:</li>
	<ul><i>
	<li>Here we go gathering nuts and berries, so early in the mornin'.</li>
	<li>Here we go gathering mad dog, and don't forget the pretzeeeels!</li>
	<li>Here we go gathering boxed wine, and don't forget the rebate!</li>
	<li>Here we go gathering champagne, and don't forget the handcuffs!</li>
	<li>Here we go gathering bourbon, and don't forget the coke!</li>
	<li>Here we go gathering vodka, and don't forget the trashbags!</li>
	<li>Here we go gathering BEER, and don't forget the beeeeeeeer!</li>
	</i></ul>
	<li>During the repeated "Let's go red! Let's go red!" the three hops must be as goofy as possible. </li>
	<li>After "1, 2, 3, 4, 5, 6... HOO-HA! (heel click)" we yell, in honor of Horn Mama Susie Lai '11:
<i>Baffroom! Iffaca! Frodown! Erfquake!</i></li>
	<li>After "Uh oh, uh oh, oh!" all must shimmy for four counts. Those unable to shimmy should instead engage in the "spoken shimmy" (started by Nicole Bardabelias '14).</li>
	<li>After the second round of shimmying, we gallop - one arm in front with rapid rhythmic steps. Typically, everyone leaves the ranks of the band,
		and runs up in front of the drum major. Occasionally, instead, we gallop in place. While we gallop we sometimes yell: <i> Our horns are big and shiny! Silver horns are nice and horny!</i></li>
	<li>When we have returned from galloping, we paddle with our horns. We chant <i>Stroke! Stroke! Stroke!</i> while the coxswain (section leaders) name off each member of the section,
    and sometimes someone will throw in a <i>me!</i> </li>
	<li>After the "stroke" chant, one of the section leaders will mimc the "Martinez" chant the trumpets do, by chanting (but preferably screaming at the top of their lungs),
		different pies. For example:<br/>
			<ul><i>"When I say pumpkin you say pie,<br/>
			when I say pecan you say pie,<br/>
			when I say apple you say pie,<br/>
			when I say cherry you say pie"</i></ul>
  <li>At the discretion of the section leader, instead of the pie chant we may instead do the "Brandon Sucks" chant in honor of Brandon Smith '19, started by Snigdha Sharma '18.<br/>
      <ul><i>"When I say Brandon you say sucks,<br/>
        when I say Brandon, you say sucks<br/>
        when I say Brandon, you say sucks<br/>
        when I say Brandon, you say sucks"</i></ul>
	<li>When the Trumpets sing their "Trumpet mater" the second time through the cadence, we turn around, march backwards, and sing (yell) to the tune of "Eight Miles Wide" by Storm Large:</li>
	<ul>
	<i>Our section is 8 miles wide<br/>
	Absolutely everyone can come inside<br/>
	When you see us coming, run and hide<br/>
	'Cuz our section is 8 miles wide.</i></li>
	</ul>
	</ul>


